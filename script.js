function add(num1, num2){
    num1 = prompt("Select first number")
    num2 = prompt("Select second number")
    return parseInt(num1 + num2)
}

function subtract(num1, num2){
    num1 = prompt("Select first number")
    num2 = prompt("Select second number")
    return parseInt(num1 - num2)
}

function multiply(num1, num2){
    num1 = prompt("Select first number")
    num2 = prompt("Select second number")
    return parseInt(num1 * num2)
}

function divide(num1, num2){
    num1 = prompt("Select number to be divide by")
    num2 = prompt("Select divider")
    return parseInt(num1 / num2)
}
